// Concatenates every prisma/migrations/*/migration.sql (in order) into one file you can paste
// directly into the Supabase SQL Editor — no Prisma CLI, no DATABASE_URL/DIRECT_URL needed on
// the machine running it. Also seeds the `_prisma_migrations` bookkeeping table with the same
// checksums Prisma itself would record, so that if `prisma migrate deploy`/`dev` is ever run
// against this database later (e.g. from a dev machine), Prisma sees these migrations as already
// applied instead of re-running (and failing on) `CREATE TABLE` for tables that already exist.
//
// Run: node scripts/build-supabase-sql.mjs
// Re-run this after adding any new migration under prisma/migrations/ to keep supabase/deploy.sql
// in sync — it is not regenerated automatically.

import { createHash, randomUUID } from "node:crypto";
import { readFileSync, readdirSync, writeFileSync, statSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const rootDir = join(dirname(fileURLToPath(import.meta.url)), "..");
const migrationsDir = join(rootDir, "prisma", "migrations");
const outputPath = join(rootDir, "supabase", "deploy.sql");

const migrationFolders = readdirSync(migrationsDir)
  .filter((name) => statSync(join(migrationsDir, name)).isDirectory())
  .sort(); // migration folder names are timestamp-prefixed, so lexical sort == chronological order

const sections = [];
const migrationRows = [];

for (const folder of migrationFolders) {
  const sqlPath = join(migrationsDir, folder, "migration.sql");
  const sql = readFileSync(sqlPath, "utf-8");
  const checksum = createHash("sha256").update(sql).digest("hex");

  sections.push(`-- ============================================================\n-- Migration: ${folder}\n-- ============================================================\n${sql.trimEnd()}\n`);

  migrationRows.push({ id: randomUUID(), checksum, migrationName: folder });
}

const migrationInserts = migrationRows
  .map(
    (row) =>
      `INSERT INTO "_prisma_migrations" ("id", "checksum", "finished_at", "migration_name", "started_at", "applied_steps_count")\nVALUES ('${row.id}', '${row.checksum}', now(), '${row.migrationName}', now(), 1);`
  )
  .join("\n");

const output = `-- AUTO-GENERATED by scripts/build-supabase-sql.mjs — do not edit by hand.
-- Regenerate with: node scripts/build-supabase-sql.mjs
--
-- One-shot setup for a brand-new, empty Supabase Postgres database. Paste this whole file into
-- the Supabase SQL Editor (Dashboard → SQL Editor → New query) and run it once.
--
-- This does NOT replace Prisma Migrate for ongoing schema changes — after this initial setup,
-- create new migrations the normal way (\`npx prisma migrate dev\`) and apply them with
-- \`npx prisma migrate deploy\` against DIRECT_URL. This file only covers the migrations that
-- existed at generation time; re-run the generator script after adding new ones.

BEGIN;

${sections.join("\n")}
-- ============================================================
-- Prisma migration bookkeeping
-- ============================================================
-- Lets a future \`prisma migrate deploy\`/\`dev\` run recognize the migrations above as already
-- applied (matching checksums) instead of re-running their CREATE TABLE statements.
CREATE TABLE "_prisma_migrations" (
    "id" VARCHAR(36) PRIMARY KEY NOT NULL,
    "checksum" VARCHAR(64) NOT NULL,
    "finished_at" TIMESTAMPTZ,
    "migration_name" VARCHAR(255) NOT NULL,
    "logs" TEXT,
    "rolled_back_at" TIMESTAMPTZ,
    "started_at" TIMESTAMPTZ NOT NULL DEFAULT now(),
    "applied_steps_count" INTEGER NOT NULL DEFAULT 0
);

${migrationInserts}

COMMIT;
`;

writeFileSync(outputPath, output);
console.log(`Wrote ${outputPath} (${migrationFolders.length} migrations combined)`);
